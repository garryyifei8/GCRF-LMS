// 获取分类名称
function getCategoryName(code) {
  if (!code) return '未分类'
  // 先尝试完整匹配
  if (categoryMap[code]) return categoryMap[code]
  // 再尝试按首字母匹配
  const firstChar = code.charAt(0)
  const category = categories.find((c) => c.code === firstChar)
  return category ? category.name : '未分类'
}

// 生成图书数据
export function generateBooks(count = 50) {
  return realBooksData.slice(0, count).map((book, index) => {
    const categoryName = getCategoryName(book.classificationCode)
    const borrowed = book.borrowedQuantity || 0
    const total = book.totalQuantity || 0

    return {
      id: book.id,
      barcode: `BK${String(book.id).padStart(8, '0')}`,
      isbn: book.isbn,
      title: book.title,
      subtitle: book.subtitle,
      author: book.author,
      publisher: book.publisher,
      category: book.classificationCode ? book.classificationCode.charAt(0) : 'Z',
      categoryName: categoryName,
      description: book.description || '',
      price: book.price,
      pages: book.pages,
      coverUrl: book.coverUrl || `https://picsum.photos/seed/${index}/200/300`,
      publishDate: book.publishDate,
      callNumber: `${book.classificationCode || 'Z'}/${book.id}`,
      location: ['一楼书库', '二楼阅览室', '三楼参考室', '四楼特藏室'][index % 4],
      totalCopies: total,
      availableCopies: book.availableQuantity || 0,
      borrowCount: Math.floor(Math.random() * 200) + 50, // 模拟借阅次数
      status: borrowed >= total ? 'borrowed' : 'available',
      tags: '',
      createdAt: new Date().toISOString(),
      updatedAt: new Date().toISOString()
    }
  })
}

// 生成图书统计数据 - 基于真实数据计算
export function generateBookStats() {
  const books = realBooksData
  const totalCopies = books.reduce((sum, b) => sum + (b.totalQuantity || 0), 0)
  const availableCopies = books.reduce((sum, b) => sum + (b.availableQuantity || 0), 0)
  const borrowedCopies = books.reduce((sum, b) => sum + (b.borrowedQuantity || 0), 0)

  return {
    totalBooks: books.length,
    totalCopies: totalCopies,
    availableCopies: availableCopies,
    borrowedCopies: borrowedCopies,
    categoriesCount: categories.length,
    newBooksThisMonth: 5,
    popularBooksCount: 20,
    zeroCirculationCount: 0
  }
}
